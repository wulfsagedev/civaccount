#!/usr/bin/env node
/**
 * generate-coverage-stats.mjs — Read validation-latest.json and write
 * src/data/coverage-stats.ts with coverage percentages and validation summary.
 *
 * Used by the methodology page to show live data quality stats.
 *
 * Usage:
 *   node scripts/generate-coverage-stats.mjs
 *   npm run generate:coverage
 */

import { readFileSync, writeFileSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPORT_PATH = join(__dirname, 'validate', 'reports', 'validation-latest.json');
const OUTPUT_PATH = join(__dirname, '..', 'src', 'data', 'coverage-stats.ts');

function main() {
  if (!existsSync(REPORT_PATH)) {
    // In CI/Vercel, validation-latest.json won't exist — write fallback stats
    console.log('validation-latest.json not found — writing fallback coverage stats.');
    const fallback = `/**
 * Auto-generated fallback by scripts/generate-coverage-stats.mjs
 * validation-latest.json was not available at build time.
 * Run: npm run validate && npm run generate:coverage
 */

export const COVERAGE_STATS: Record<string, { label: string; present: number; total: number }> = {};

export const VALIDATION_SUMMARY = {
  errors: 0,
  warnings: 0,
  info: 0,
  regressions: 0,
  totalChecks: 0,
  totalCouncils: 317,
  parityAverage: 0,
  timestamp: '${new Date().toISOString().split('T')[0]}',
};
`;
    writeFileSync(OUTPUT_PATH, fallback);
    console.log(`Fallback coverage stats written to ${OUTPUT_PATH}`);
    return;
  }

  const report = JSON.parse(readFileSync(REPORT_PATH, 'utf-8'));
  const byField = report.completeness?.by_field || {};
  const summary = report.summary || {};

  // Map field groups to human-readable labels
  const fieldLabels = {
    council_tax: 'Council tax rates',
    budget: 'Service budgets',
    chief_executive_salary: 'CEO salary',
    councillor_basic_allowance: 'Councillor basic allowance',
    total_allowances_cost: 'Total allowances cost',
    councillor_allowances_detail: 'Councillor allowances detail',
    salary_bands: 'Salary bands',
    staff_fte: 'Staff headcount (FTE)',
    top_suppliers: 'Top suppliers',
    grant_payments: 'Grant payments',
    service_spending: 'Service spending detail',
    performance_kpis: 'Performance KPIs',
    waste_destinations: 'Waste destinations',
    documents: 'Official documents',
    sources: 'Source links',
    cabinet: 'Cabinet members',
    budget_gap: 'Budget gap / savings',
    savings_target: 'Savings target',
  };

  const coverageEntries = [];

  for (const [field, label] of Object.entries(fieldLabels)) {
    const data = byField[field];
    if (data) {
      coverageEntries.push(`  '${field}': { label: '${label}', present: ${data.present}, total: ${data.total} },`);
    }
  }

  const ts = `/**
 * Auto-generated by scripts/generate-coverage-stats.mjs
 * Last generated: ${new Date().toISOString().split('T')[0]}
 * Source: scripts/validate/reports/validation-latest.json
 *
 * DO NOT EDIT MANUALLY — run: npm run generate:coverage
 */

export const COVERAGE_STATS: Record<string, { label: string; present: number; total: number }> = {
${coverageEntries.join('\n')}
};

export const VALIDATION_SUMMARY = {
  errors: ${summary.errors || 0},
  warnings: ${summary.warnings || 0},
  info: ${summary.info || 0},
  regressions: ${summary.regressions || 0},
  totalChecks: ${summary.total_checks || 0},
  totalCouncils: ${summary.total_councils || 317},
  parityAverage: ${summary.parity?.average || 0},
  timestamp: '${report.timestamp?.split('T')[0] || new Date().toISOString().split('T')[0]}',
};
`;

  writeFileSync(OUTPUT_PATH, ts);
  console.log(`Coverage stats written to ${OUTPUT_PATH}`);
  console.log(`  Fields tracked: ${coverageEntries.length}`);
  console.log(`  Validation: ${summary.errors || 0} errors, ${summary.warnings || 0} warnings`);
}

main();
