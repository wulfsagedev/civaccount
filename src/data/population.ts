// Helper function to get population by council name
export function getPopulation(councilName: string): number | undefined {
  // Direct match
  if (populationData[councilName]) {
    return populationData[councilName];
  }

  // Try common variations
  const normalizedName = councilName
    .replace(' Council', '')
    .replace(' Borough Council', '')
    .replace(' District Council', '')
    .replace(' County Council', '')
    .replace(' City Council', '')
    .replace(', City of', '')
    .replace(' UA', '')
    .trim();

  return populationData[normalizedName];
}

// Get total population of England (all councils)
export function getTotalPopulation(): number {
  return Object.values(populationData).reduce((sum, pop) => sum + pop, 0);
}
